package Java_Basic;
/*
 * 출력
 * Println 내용 끝에 개행이 추가
 * Printf 내용 끝에 개행이 없음
 * Print 내용 끝에 개행이 없음
 */
public class Java_01_Output_Print {
	public static void main(String args[]) {
		System.out.println("Hello");
		System.out.print("Hello");
		System.out.printf("Hello");
		System.out.println("Hello");
		// 전체 출력 값의 끝에 println의 개행이 포함되어있는 것을 볼 수 있다. 
	}
}
